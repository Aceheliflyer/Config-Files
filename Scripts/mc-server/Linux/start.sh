# !/bin/bash
# This script starts a minecraft server on Linux machines
# To start servers with this script, place it next to your server jar file.
# Syntax: start.sh <instance name>

# CONFIG
INSTANCE_NAME=$1
MINECRAFT_HOME="$PWD"

echo Minecraft directory: $MINECRAFT_HOME
cd $MINECRAFT_HOME


# java -Xms2G -Xmx2G -jar $1 nogui
java -Xms10G -Xmx10G -XX:+UseG1GC -XX:+ParallelRefProcEnabled -XX:MaxGCPauseMillis=200 -XX:+UnlockExperimentalVMOptions -XX:+DisableExplicitGC -XX:+AlwaysPreTouch -XX:G1NewSizePercent=30 -XX:G1MaxNewSizePercent=40 -XX:G1HeapRegionSize=8M -XX:G1ReservePercent=20 -XX:G1HeapWastePercent=5 -XX:G1MixedGCCountTarget=4 -XX:InitiatingHeapOccupancyPercent=15 -XX:G1MixedGCLiveThresholdPercent=90 -XX:G1RSetUpdatingPauseTimePercent=5 -XX:SurvivorRatio=32 -XX:+PerfDisableSharedMem -XX:MaxTenuringThreshold=1 -Dusing.aikars.flags=https://mcflags.emc.gs -Daikars.new.flags=true -jar $1 nogui
